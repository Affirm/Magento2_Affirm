namespace Astound\Affirm\Model\Resolver\StoreConfig;

use Astound\Affirm\Model\Config;
use Magento\Framework\GraphQl\Config\Element\Field;
use Magento\Framework\GraphQl\Query\ResolverInterface;
use Magento\Framework\GraphQl\Schema\Type\ResolveInfo;

/**
 * Class CountryCode
 *
 * @package Astound\Affirm\Model\Resolver\StoreConfig
 */
class CountryCode implements ResolverInterface
{
    public Config $config;

    /**
     * Locale Constructor
     *
     * @param \Astound\Affirm\Model\Config $config
     */
    public function __construct(
        Config $config
    ) {
        $this->config = $config;
    }

    /**
     * @inheritDoc
     */
    public function resolve(
        Field $field,
        $context,
        ResolveInfo $info,
        ?array $value = null,
        ?array $args = null
    ) {
        return $this->config->getCountryCode();
    }
}
